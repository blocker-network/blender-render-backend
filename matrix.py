# Add Object to Scene
def createMesh (idx, point):
    name = "cubert"
    mesh = bpy.data.meshes.new(name + str(idx))
    mesh.from_pydata(verts, [], faces)

    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)

    bpy.context.view_layer.objects.active = obj
    #obj.select = True

    obj.location = [i * -1 for i in point.getOffset()]

    translation_matrix = Matrix.Translation((point.getX(), point.getY(), point.getZ()))
    obj.matrix_world @= translation_matrix

    rotation_mat = Matrix.Rotation(math.radians(0), 4, 'X')
    obj.matrix_world @= rotation_mat



